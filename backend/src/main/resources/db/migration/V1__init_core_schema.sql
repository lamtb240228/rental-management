CREATE UNIQUE INDEX uk_rental_contracts_active_room ON rental_contracts(room_id) WHERE status = 'ACTIVE' AND deleted_at IS NULL;

CREATE INDEX idx_contract_tenants_contract_id ON contract_tenants(contract_id);
CREATE INDEX idx_contract_tenants_tenant_id ON contract_tenants(tenant_id);
CREATE UNIQUE INDEX uk_contract_tenants_primary ON contract_tenants(contract_id) WHERE primary_tenant = TRUE;

CREATE INDEX idx_utility_readings_room_id ON utility_readings(room_id);
CREATE UNIQUE INDEX uk_utility_readings_room_period_active ON utility_readings(room_id, billing_year, billing_month) WHERE deleted_at IS NULL;

CREATE INDEX idx_invoices_contract_id ON invoices(contract_id);
CREATE INDEX idx_invoices_status ON invoices(status);
CREATE INDEX idx_invoices_due_date ON invoices(due_date);
CREATE UNIQUE INDEX uk_invoices_contract_period_active ON invoices(contract_id, billing_year, billing_month) WHERE deleted_at IS NULL;

CREATE INDEX idx_invoice_items_invoice_id ON invoice_items(invoice_id);
CREATE INDEX idx_invoice_items_item_type ON invoice_items(item_type);

CREATE INDEX idx_payments_invoice_id ON payments(invoice_id);
CREATE INDEX idx_payments_status ON payments(payment_status);
CREATE INDEX idx_payments_paid_at ON payments(paid_at);
CREATE UNIQUE INDEX uk_payments_transaction_reference ON payments(transaction_reference) WHERE transaction_reference IS NOT NULL;

