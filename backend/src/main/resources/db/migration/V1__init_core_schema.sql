CREATE INDEX idx_properties_status ON properties(status);

CREATE INDEX idx_rooms_property_id ON rooms(property_id);
CREATE INDEX idx_rooms_status ON rooms(status);
CREATE UNIQUE INDEX uk_rooms_property_room_number_active ON rooms(property_id, lower(room_number)) WHERE deleted_at IS NULL;

CREATE INDEX idx_tenants_landlord_id ON tenants(landlord_id);
CREATE INDEX idx_tenants_user_account_id ON tenants(user_account_id);
CREATE INDEX idx_tenants_status ON tenants(status);
CREATE UNIQUE INDEX uk_tenants_landlord_identity_active ON tenants(landlord_id, identity_number) WHERE deleted_at IS NULL AND identity_number IS NOT NULL;

CREATE INDEX idx_rental_contracts_room_id ON rental_contracts(room_id);
CREATE INDEX idx_rental_contracts_status ON rental_contracts(status);
